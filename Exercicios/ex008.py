m = float(input('Digite uma distância em metros: '))
km = m / 1000
hm = m / 100
da = m / 10
dm = m * 10
cm = m * 100
mm = m * 1000
print('A distância convertida em Kilometro {}km'.format(km))
print('a distância convertida em Hectômetro {}hm'.format(hm))
print('A distância convertida em Decâmetros é {}da'.format(da))
print('A distância convertida em Decímetro é {}dm'.format(dm))
print('A distâmcia convertida em Cemtímetro é {}cm'.format(cm))
print('A distância convertida em Milímetro é {}mm'.format(mm))
